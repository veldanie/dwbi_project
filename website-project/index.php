<h1><center>Data that count</center></h1> 
<h2><center>ICT data for development</center></h2> 

<p>The world we live in is changing at an ever increasing pace and information and communication technologies (ICTs) are playing a key role in current economic development (World Bank, 2009) and social transformations (Castells, 1996; Graham & Dutton, 2014).</p> 

<p> <img src="img/SDGs.jpg" width="33%" vspace="15" align="left" style="margin-right:20px;" /> The importance of ICTs in several areas of economic and social development has been recognized in the Sustainable Development Goals (SDGs), the global development agenda that will govern development efforts in the 2015-2030 period. This UN-led agenda has an enormous importance in terms of resource mobilization and investment channeling to selected areas and countries. In order to inform decisions relating to the SDGs, a global indicator framework has been defined. This project looks at some of the ICT indicators defined in this context and tries to fill in some of the data gaps.</p>

<p>Data already available show that there is an alarming "digital divide", that is, a big difference between the countries that are well connected and make use of ICTs and many developing countries in which most of the population still do not have access to, or make use of, ICTs. Using the data from International Telecommunication Union (ITU), we produced the map below to illustrate the digital divide in the world today, according to the percentage of Internet Users.</p>
<img src="img/digital_divide.png" width="100%"><br>

<p>The digital divide has an impact that goes well beyond the ICT sector and has profound implications, inter alia, in the type and quality of the employment created and the efficiency of the agricultural, health and public sectors.</p>

<p> Our project aims to contribute to these issues by addressing to concrete and current business and research questions: <br>
<ol id="customlist">
  <li><b> Analyze the link between ICTs and other sectors:</b> identify key macroeconomic indicators strongly related to ICT development in order to inform policy-makers and international organizations. We are trying to contribute to the following question from these stakeholders: what are the spill-over effects that improvements in ICTs may have in health, education, economic development, etc.? </li>
  <li><b> Inform and guide investments in the ICT sector to those countries most needing it:</b> fill in the data gaps in order to identify those countries where ICT development is the lowest and inform investment decisions from international donors, such as development banks (e.g. the <a href="http://www.worldbank.org">World Bank</a>, the <a href="http://www.iadb.org">Inter-American Development Bank</a>, the <a href="https://www.adb.org">Asian Development Bank </a>  or the <a href="http://www.afdb.org">African Development Bank </a>).</li>
</ol>
</p>

